DECLARE @TRH1 AS DATETIME='{TARIH1}'
DECLARE @TRH2 AS DATETIME='{TARIH2}'
DECLARE @KOD1 NVARCHAR(100)='{KOD1}'
DECLARE @ProductGroup AS NVARCHAR(100)='{ProductGroup}'
DECLARE @URUNADI AS NVARCHAR(100)='{URUNADI}'
DECLARE @SUBEADI AS NVARCHAR(100)='{SUBEADI}'
SELECT * FROM (
SELECT 
CAST(DATEPART(MONTH, [date]) AS nvarchar(2))+'.'+CAST(DATEPART(DAY, [date]) AS nvarchar(2))  as Tarih,
SUM(Quantity) as Miktar,
Sum(Quantity*Price) as Tutar
 FROM [dbo].[Bill]
WHERE Date>=@TRH1  AND Date<=@TRH2  #KOD1WHERE# #ProductGroupWHERE# #URUNADIWHERE# #BELÝRLÝSAATLER# #SUBEADIWHERE#
group by
DATEPART(DAY, [date]),
DATEPART(MONTH, [date]) ) AS T
ORDER BY Tarih